// This build script always runs on the host with std available, so the workspace-wide
// std-vs-core/alloc and expect-used lints don't apply here.
#![allow(
    clippy::std_instead_of_core,
    clippy::std_instead_of_alloc,
    clippy::expect_used
)]

fn main() {
    #[cfg(feature = "generate-protobuf")]
    generate::run()
        .expect("failed to regenerate protobuf bindings from proto/metrics/intake_v3.proto");
    #[cfg(not(feature = "generate-protobuf"))]
    println!("cargo:rerun-if-changed=build.rs");
}

/// Regenerates `tests/pb/mod.rs` from `../../proto/metrics/intake_v3.proto`. Only compiled when the
/// `generate-protobuf` feature is enabled; normal builds and test runs use the checked-in
/// `tests/pb/mod.rs`.
#[cfg(feature = "generate-protobuf")]
mod generate {
    use std::error::Error;
    use std::{env, fs, path::Path};

    const HEADER: &str = "// This file is @generated by prost-build from `proto/metrics/intake_v3.proto`. Do not edit it
// directly: regenerate it with `cargo build -p dd-metrics-v3 --features generate-protobuf` after
// changing the proto file.
#![allow(dead_code, clippy::all, clippy::pedantic, clippy::nursery)]

";

    /// Pinned by `tasks/codegen.py`'s `protoc_version`; keep in sync with that file so the
    /// Go/Java and Rust bindings are generated by the exact same `protoc`.
    const PROTOC_VERSION: &str = "21.12";

    pub fn run() -> Result<(), Box<dyn Error>> {
        let cur_working_dir = env::var("CARGO_MANIFEST_DIR")?;
        let crate_root = Path::new(&cur_working_dir);
        let repo_root = crate_root.join("../..");

        // Reuse the repo's own pinned protoc.
        let protoc = repo_root.join(format!("toolchains/bin/protoc{PROTOC_VERSION}"));
        if !protoc.exists() {
            return Err(format!(
                "protoc not found at {}; run `inv codegen.install-protoc` from the repo root first",
                protoc.display()
            )
            .into());
        }
        env::set_var("PROTOC", &protoc);

        let toolchain_include_dir = repo_root.join("toolchains/include/proto");

        let out_dir = crate_root.join("tests/pb");
        fs::create_dir_all(&out_dir)?;

        let proto_root = crate_root.join("../../proto/metrics");
        let proto_file = proto_root.join("intake_v3.proto");

        let mut config = prost_build::Config::new();
        config.out_dir(&out_dir);

        println!("cargo:rerun-if-changed={}", proto_file.display());
        config.compile_protos(&[proto_file], &[proto_root, toolchain_include_dir])?;

        // prost-build names the output file after the proto's package
        // (`datadoghq.api.metrics.v3.rs`); rename it to `mod.rs` so `tests/parity.rs` can
        // pull it in as a plain submodule.
        let generated = out_dir.join("datadoghq.api.metrics.v3.rs");
        let mod_rs = out_dir.join("mod.rs");
        fs::rename(&generated, &mod_rs)?;

        prepend_to_file(HEADER.as_bytes(), &mod_rs)?;

        Ok(())
    }

    fn prepend_to_file(data: &[u8], file_path: &Path) -> Result<(), Box<dyn Error>> {
        use std::io::{Read, Write};

        let mut f = fs::File::open(file_path)?;
        let mut content = data.to_owned();
        f.read_to_end(&mut content)?;

        let mut f = fs::File::create(file_path)?;
        f.write_all(content.as_slice())?;
        Ok(())
    }
}
